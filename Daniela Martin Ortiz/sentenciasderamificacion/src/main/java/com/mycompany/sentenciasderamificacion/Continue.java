public class Continue {

  
    public static void main(String[] args) {
        for (int m= 1;m<=10;m++)
        { 
          if(m==7)
          {
             System.out.println("El programa continuara ");
            continue;
            }
             System.out.println(" "+m);
           }
    }
  } 
  