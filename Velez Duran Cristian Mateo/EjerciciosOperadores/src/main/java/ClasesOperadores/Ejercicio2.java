
package ClasesOperadores;


public class Ejercicio2 {


    public static void main(String[] args) {
        int Variable1 = 9, Variable2 = 7, Resultado;
        Resultado = Variable1+Variable2;
        System.out.println("9+7: "+ Resultado);
        Resultado = Variable1-Variable2;
        System.out.println("9-7: "+ Resultado);
        Resultado = Variable1 * Variable2;
        System.out.println("9*7: "+ Resultado);
        Resultado = Variable1 / Variable2;
        System.out.println("9/7: "+ Resultado);
    }
    
}
