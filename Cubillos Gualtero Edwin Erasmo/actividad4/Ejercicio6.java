package actividad4;

public class Ejercicio6 {
    public static void main(String[] args) {

        int a = 5;
        int b = 2;

        b += a;
        System.out.println(b);
        System.out.println();

        b = 2;
        b -= a;
        System.out.println(b);
        System.out.println();

        b = 2;
        b *= a;
        System.out.println(b);
        System.out.println();

        double b2 = 2.0;
        b2 /= a;
        System.out.println(b2);
        System.out.println();

        b = 2;
        b %= a;
        System.out.println(b);
    }
}
