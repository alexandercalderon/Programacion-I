package actividad4;

public class Ejercicio5 {
    public static void main(String[] args) {

        int a = 5;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);
        System.out.println();

        a = 5;
        b = a++;
        System.out.println(a);
        System.out.println(b);
        System.out.println();

        a = 5;
        b = --a;
        System.out.println(a);
        System.out.println(b);
        System.out.println();

        a = 5;
        b = a--;
        System.out.println(a);
        System.out.println(b);
    }
}
