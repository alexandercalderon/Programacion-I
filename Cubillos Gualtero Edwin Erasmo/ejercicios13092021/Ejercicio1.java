package ejercicios13092021;

public class Ejercicio1 {
    public static void main(String[] args) {

        int A = 5, B = 3, C = -12;

        System.out.println("a) A > 3  -> " + (A > 3));
        System.out.println("b) A > C  -> " + (A > C));
        System.out.println("c) A < C  -> " + (A < C));
        System.out.println("d) B < C  -> " + (B < C));
        System.out.println("e) B != C  -> " + (B != C));
        System.out.println("f) A == 3  -> " + (A == 3));
        System.out.println("g) A * B == 15  -> " + (A * B == 15));
        System.out.println("h) A * B == -30  -> " + (A * B == -30));
        System.out.println("i) C / B < A  -> " + (C / B < A));
        System.out.println("j) C / B == -10  -> " + (C / B == -10));
        System.out.println("k) C / B == -4  -> " + (C / B == -4));
        System.out.println("l) A + B + C == 5  -> " + (A + B + C == 5));
        System.out.println("m) (A + B == 8) && (A - B == 2)  -> " + ((A + B == 8) && (A - B == 2)));
        System.out.println("n) (A + B == 8) || (A - B == 6)  -> " + ((A + B == 8) || (A - B == 6)));
        System.out.println("o) A > 3 && B > 3 && C < 3  -> " + (A > 3 && B > 3 && C < 3));
        System.out.println("p) A > 3 && B >= 3 && C < 3  -> " + (A > 3 && B >= 3 && C < 3) );
    }
}
